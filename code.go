package jac

import "log"

type codeMap map[uint64]uint64

func (m codeMap) reverse() codeMap {
	var newMap = make(map[uint64]uint64, len(m))
	for key, value := range m {
		newMap[value] = key
	}
	if len(m) != len(newMap) {
		log.Fatalln("map is wrong")
	}
	return newMap
}

